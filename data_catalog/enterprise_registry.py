def build_enterprise_catalog() -> EnterpriseDataCatalog:
    return EnterpriseDataCatalog(datasets=list(ENTERPRISE_DATASETS))


def enterprise_dataset_by_id(dataset_id: str) -> EnterpriseDataset | None:
    normalized = dataset_id.strip().lower()
    return next((dataset for dataset in ENTERPRISE_DATASETS if dataset.dataset_id.lower() == normalized), None)


def datasets_by_product_capability(capability: ProductCapability) -> list[EnterpriseDataset]:
    return [dataset for dataset in ENTERPRISE_DATASETS if capability in dataset.product_capabilities]


def datasets_by_tier(tier: int) -> list[EnterpriseDataset]:
    return [dataset for dataset in ENTERPRISE_DATASETS if dataset.tier == tier]


def catalog_gaps(catalog: EnterpriseDataCatalog | None = None) -> list[EnterpriseDataset]:
    source = catalog or build_enterprise_catalog()
    return [
        dataset
        for dataset in source.datasets
        if dataset.status_ingestao in {"not_started", "failed", "manual_review"} or dataset.lacunas
    ]


def prioritization_table(catalog: EnterpriseDataCatalog | None = None) -> list[PrioritizationRow]:
    source = catalog or build_enterprise_catalog()
    rows = [
        PrioritizationRow(
            dataset_id=dataset.dataset_id,
            tier=dataset.tier,
            prioridade_produto=dataset.prioridade_produto,
            score_confiabilidade=dataset.score_confiabilidade,
            status_ingestao=dataset.status_ingestao,
            capacidades_produto=dataset.product_capabilities,
            impacto_comercial=_impact(dataset.prioridade_produto, dataset.tier),
            lacunas_bloqueantes=dataset.lacunas + dataset.business_metadata.lacunas_conhecidas,
        )
        for dataset in source.datasets
    ]
    return sorted(rows, key=lambda row: (row.prioridade_produto, row.score_confiabilidade), reverse=True)


def coverage_summary(catalog: EnterpriseDataCatalog | None = None) -> dict[str, list[str]]:
    source = catalog or build_enterprise_catalog()
    return {
        "temporal": sorted(
            {f"{dataset.cobertura_temporal.start}:{dataset.cobertura_temporal.end}" for dataset in source.datasets}
        ),
        "geografica": sorted(
            {f"{dataset.cobertura_geografica.start}:{dataset.cobertura_geografica.end}" for dataset in source.datasets}
        ),
        "granularidade": sorted({dataset.granularidade for dataset in source.datasets}),
    }


def required_capabilities_present(
    capabilities: Iterable[ProductCapability],
    catalog: EnterpriseDataCatalog | None = None,
) -> bool:
    source = catalog or build_enterprise_catalog()
    present = {capability for dataset in source.datasets for capability in dataset.product_capabilities}
    return set(capabilities).issubset(present)


def _impact(priority: int, tier: int) -> Literal["critical", "high", "medium", "low"]:
    if tier == 1 and priority >= 90:
        return "critical"
    if priority >= 75:
        return "high"
    if priority >= 55:
        return "medium"
    return "low"
