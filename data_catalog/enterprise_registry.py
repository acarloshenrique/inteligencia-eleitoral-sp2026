def _coverage(start: str, end: str, notes: str = "") -> Coverage:
    return Coverage(start=start, end=end, notes=notes)


def _technical(
    dataset_id: str,
    layer: str,
    partitioning: list[str],
    lineage_upstream: list[str] | None = None,
    lineage_downstream: list[str] | None = None,
    incremental_key: str | None = None,
) -> TechnicalMetadata:
    return TechnicalMetadata(
        owner_logico="data-platform",
        schema_uri=f"data_catalog/schemas/{dataset_id}.json",
        storage_path=f"lake/{layer}/{dataset_id}",
        particionamento=partitioning,
        incremental_key=incremental_key,
        lineage_upstream=lineage_upstream or [],
        lineage_downstream=lineage_downstream or [],
    )


def _business(
    finalidade: str,
    capabilities: list[ProductCapability],
    questions: list[str],
    gaps: list[str] | None = None,
    doc: str = "",
) -> BusinessMetadata:
    return BusinessMetadata(
        finalidade_produto=finalidade,
        funcionalidades_suportadas=capabilities,
        perguntas_respondidas=questions,
        lacunas_conhecidas=gaps or [],
        documentacao_negocio=doc,
    )


def _rules(primary_key: list[str], freshness_threshold: float = 1.0) -> list[QualityRule]:
    return [
        QualityRule(
            rule_id="schema_required_columns",
            description="All required analytical columns must be present before promotion.",
            severity="critical",
            check_type="schema",
            target_columns=primary_key,
        ),
        QualityRule(
            rule_id="primary_key_uniqueness",
            description="Primary key must be unique inside the declared coverage and partition.",
            severity="high",
            check_type="uniqueness",
            target_columns=primary_key,
            threshold=0.995,
        ),
        QualityRule(
            rule_id="freshness_within_expected_period",
            description="Dataset must be refreshed according to its declared periodicity or marked stale.",
            severity="medium",
            check_type="freshness",
            threshold=freshness_threshold,
        ),
    ]


def _dep(
    dataset_id: str,
    dependency_type: str,
    join_keys: list[str],
    confidence: float,
    notes: str = "",
) -> DatasetDependency:
    return DatasetDependency(
        dataset_id=dataset_id,
        dependency_type=dependency_type,  # type: ignore[arg-type]
        join_keys=join_keys,
        join_confidence=confidence,
        notes=notes,
    )


